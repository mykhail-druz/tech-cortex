// Category slug to templates mapping (includes aliases for backward compatibility)
const CATEGORY_TEMPLATES: Record<string, Omit<CreateSpecificationTemplate, 'category_id'>[]> = {
  cpu: CPU_TEMPLATES,
  processors: CPU_TEMPLATES,
  gpu: GPU_TEMPLATES,
  'graphics-cards': GPU_TEMPLATES,
  ram: RAM_TEMPLATES,
  memory: RAM_TEMPLATES,
  motherboard: MOTHERBOARD_TEMPLATES,
  motherboards: MOTHERBOARD_TEMPLATES,
  mainboard: MOTHERBOARD_TEMPLATES,
  mainboards: MOTHERBOARD_TEMPLATES,
};

/**
 * Get templates for a specific category slug
 */
export function getTemplatesForCategorySlug(
  categorySlug: string
): Omit<CreateSpecificationTemplate, 'category_id'>[] | null {
  return CATEGORY_TEMPLATES[categorySlug] || null;
}

/**
 * Check if templates exist for a category
 */
export function hasTemplatesForCategory(categorySlug: string): boolean {
  return categorySlug in CATEGORY_TEMPLATES;
}

/**
 * Get all available category slugs that have templates (only primary categories for UI)
 */
export function getAvailableCategorySlugs(): string[] {
  return Object.keys(PRIMARY_CATEGORY_TEMPLATES);
}

/**
 * Get all templates for all categories
 */
export function getAllCategoryTemplates(): Record<
  string,
  Omit<CreateSpecificationTemplate, 'category_id'>[]
> {
  return CATEGORY_TEMPLATES;
}
